// PTQ Integer Inference Implementation Guide
/*
 * PTQ Model Integer Inference Flow:
 * 1. Input: Convert to int32
 * 2. For each layer:
 *    a. Linear: x = x @ W_scaled + b_scaled (int32 arithmetic)
 *    b. Quantization: x = (x + zero_point) / scale_int (compensation absorbed)
 *    c. Clamp: x = clamp(x, qmin, qmax)
 *    d. ReLU: x = max(0, x)
 * 3. Output: Final quantized result
 *
 * Key difference from training quantization:
 * - Linear layer scaling compensation is absorbed in scale_int
 * - No separate division by scale_factor needed
 * - Pure integer arithmetic throughout
 */

